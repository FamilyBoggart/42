int	ft_putnbr(int number)
{
	int	n;
	int	res;

	n = 0;
	res = 0;
	if (n < 0)
		n++;
	while (number > 0)
	{
		n++;
		number = number / 10;
	}
	write (1, &number, n);
	return (n);
}